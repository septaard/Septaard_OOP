    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        login login = new login();
        String uname = null;
        String pw = null;
        if (txtUsername.getText().isBlank() == false) {
            uname = txtUsername.getText();
        }
        if (txtpassword.getText().isBlank() == false) {
            pw = txtpassword.getText();
        }
        try {
            boolean isAuthenticated = login.ceklogin(uname, pw);
            if (isAuthenticated) {
                JOptionPane.showMessageDialog(rootPane, "\tlogin berhasil\nselamat datang\n" + login.nama + " !");
            }
        } catch (Exception a) {
            System.out.println(a.getMessage());
            JOptionPane.showMessageDialog(rootPane, "\tLogin Gagal\n\tUsername/Password \n\ttidak boleh kosong");
        }
    }//GEN-LAST:event_btnLoginActionPerformed
